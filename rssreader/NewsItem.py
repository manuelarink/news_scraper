class NewsItem:

    def __init__(self, date="", description="", url="", author="",title="", text="", category="", copyright="", source=""):
        self.date = date
        self.description = description
        self.url = url
        self.author = author
        self.title = title
        self.text = text
        self.category = category
        self.copyright = copyright
        self.source = source

